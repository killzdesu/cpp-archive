vector<int> climbingLeaderboard(vector<int> scores, vector<int> alice) {
  vector<int> ans;
  set<int> s;
  vector<int> v;
  for(auto x: scores) s.insert(x);
  for(auto x: s) v.pb(x);
  for(auto sc: alice){
    auto it = lower_bound(all(v), sc);
    if(*it == sc) ans.pb(len(v)-(it-v.begin()));
    else ans.pb(len(v)-(it-v.begin())+1);
  }
  dump_all(ans);
  return ans;
}

